"""
==============================================================================
LAB 1：Function Calling 基礎 - 互動式聊天主程式
==============================================================================

📌 本檔案功能：
    實作一個互動式命令列聊天程式，展示 Function Calling 的完整流程：
    使用者輸入 → LLM 判斷 → 工具呼叫 → 執行工具 → 回覆使用者

📖 學習重點：
    1. 如何將 LLM 輸出解析為結構化的工具呼叫
    2. 如何將工具執行結果餵回 LLM 生成最終回覆

🔧 執行方式：
    在專案根目錄執行：python -m lab1.run_chat
"""

# ==============================================================================
# 匯入模組
# ==============================================================================

# 從 common 模組匯入共用功能
from common.call_llm import call_llm              # 呼叫 vLLM 服務的函式
from common.prompts import system_prompt, tool_result_message  # 提示詞模板
from common.utils import extract_json_block, pretty            # 工具函式
from common.tools import TOOL_REGISTRY                         # 工具函式註冊表


def main():
    """
    主函式：實作互動式聊天迴圈
    
    流程說明：
    ┌─────────────────────────────────────────────────────────────────┐
    │  1. 建立對話歷史（包含 system prompt）                           │
    │  2. 進入主迴圈：                                                 │
    │     a. 讀取使用者輸入                                           │
    │     b. 呼叫 LLM 生成回應                                        │
    │     c. 判斷是否為工具呼叫                                       │
    │        - 是 → 執行工具 → 餵回結果 → 生成最終回覆                │
    │        - 否 → 直接顯示回應                                      │
    └─────────────────────────────────────────────────────────────────┘
    """

    # ==========================================================================
    # Step 1: 初始化對話歷史
    # ==========================================================================
    # messages 是對話歷史列表，格式遵循 OpenAI Chat API 標準
    # 每個訊息包含 "role"（system/user/assistant）和 "content"（訊息內容）
    # 
    # system_prompt() 回傳系統提示詞，包含：
    #   - 角色設定（訂單客服助理）
    #   - 可用工具清單和 schema
    #   - 輸出格式規則（何時輸出 JSON、何時追問、何時直接答）
    messages = [{"role": "system", "content": system_prompt()}]

    # ==========================================================================
    # Step 2: 主迴圈 - 持續與使用者互動
    # ==========================================================================
    print("LAB1: Function Calling 基礎（輸入 exit 離開）")
    print("=" * 50)
    print("提示：試試輸入「幫我查訂單 A123456789 狀態」")
    print("=" * 50)
    
    while True:
        # ----------------------------------------------------------------------
        # 2a. 讀取使用者輸入
        # ----------------------------------------------------------------------
        user = input("\nUser> ").strip()
        
        # 檢查是否要離開
        if user.lower() in ("exit", "quit"):
            print("掰掰！")
            break
        
        # 忽略空白輸入
        if not user:
            continue

        # ----------------------------------------------------------------------
        # 2b. 將使用者訊息加入對話歷史，並呼叫 LLM
        # ----------------------------------------------------------------------
        # 將使用者的輸入加入對話歷史
        messages.append(
            {
                "role": "user", 
                "content": user
            }
        )

        # 呼叫 LLM 生成回應
        # call_llm() 會將整個對話歷史送給 vLLM 服務，讓它根據上下文生成回應
        out = call_llm(messages)

        # ----------------------------------------------------------------------
        # 2c. 嘗試從 LLM 輸出中提取 JSON（判斷是否為工具呼叫）
        # ----------------------------------------------------------------------
        # extract_json_block() 會嘗試從文字中提取 JSON 物件
        # 如果 LLM 輸出了工具呼叫 JSON，這裡會解析成 dict
        # 如果 LLM 輸出純文字回覆，這裡會回傳 None
        tool_call = extract_json_block(out)

        # ======================================================================
        # 分支處理：有工具呼叫 vs 無工具呼叫
        # ======================================================================
        
        if tool_call:
            # ==================================================================
            # 分支 A：LLM 輸出了工具呼叫 JSON
            # ==================================================================
            
            # ------------------------------------------------------------------
            # Step A1: 執行工具函式
            # ------------------------------------------------------------------
            # 從 tool_call 中提取工具名稱和參數
            name = tool_call.get("name")    # 例如 "get_order_status"
            args = tool_call.get("arguments", {})  # 例如 {"order_id": "A123456789"}
            
            if name not in TOOL_REGISTRY:
                print("\nAssistant> （工具不存在，請再試一次）")
                continue
            
            print("\n[TOOL CALL]", name, pretty(args))
            
            # 從 TOOL_REGISTRY 中取得對應的函式並執行
            # TOOL_REGISTRY 是一個 dict，key 是工具名稱，value 是函式
            # 例如 TOOL_REGISTRY["get_order_status"] 會取得 get_order_status 函式
            tool_fn = TOOL_REGISTRY[name]
            
            # **args 會將 dict 展開為 keyword arguments
            # 例如 tool_fn(**{"order_id": "A123456789"}) 等同於 tool_fn(order_id="A123456789")
            result = tool_fn(**args)
            
            print("\n[TOOL RESULT]", pretty(result))
            
            # ------------------------------------------------------------------
            # Step A2: 將工具結果餵回 LLM，讓它生成最終回覆
            # ------------------------------------------------------------------
            # 這是 Function Calling 的關鍵步驟！
            # LLM 需要知道工具執行的結果，才能生成對使用者有意義的回覆
            #
            # 例如：
            #   工具回傳：{"ok": True, "status": "已出貨", "tracking_no": "TWD12345678"}
            #   LLM 應該回覆：「您的訂單 A123456789 已出貨，物流單號是 TWD12345678」
            
            # 將工具呼叫（作為 assistant 訊息）加入對話歷史
            messages.append({"role": "assistant", "content": pretty(tool_call)})
            
            # 將工具結果（作為 user 訊息，模擬系統回傳）加入對話歷史
            # tool_result_message() 會格式化工具結果，並指示 LLM 如何回覆
            messages.append({"role": "user", "content": tool_result_message(name, result)})
            
            # 呼叫 LLM 生成最終回覆
            final = call_llm(messages)
            
            # 將最終回覆加入對話歷史
            messages.append({"role": "assistant", "content": final})
            
            print("\nAssistant>", final)
            
        else:
            # ==================================================================
            # 分支 B：LLM 沒有輸出工具呼叫（直接文字回覆）
            # ==================================================================
            # 這可能是：
            #   1. LLM 判斷不需要工具（例如閒聊、概念問題）
            #   2. LLM 正在追問缺少的參數
            #   3. LLM 直接回答使用者的問題
            
            # 將回覆加入對話歷史
            messages.append({"role": "assistant", "content": out})
            
            print("\nAssistant>", out)


# ==============================================================================
# 程式進入點
# ==============================================================================
if __name__ == "__main__":
    main()
